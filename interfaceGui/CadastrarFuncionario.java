    private void btnAtualizarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarFuncActionPerformed
        String nome = nomeFuncField.getText().trim();
        String cpf = cpfFuncField.getText().trim();
        String endereco = enderecoFuncField.getText().trim();
        String telefone = telefoneFuncField.getText().trim();
        String nivel = cargoField.getText().trim();

        if (validaCampos(nome, cpf, telefone, endereco, nivel)) {
            gerenciadorFunc.atualizarFunc(idFunc, nome, cpf, telefone, endereco, nivel);
            
            limparCampos();
        }
    }//GEN-LAST:event_btnAtualizarFuncActionPerformed

