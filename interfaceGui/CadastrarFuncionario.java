    private void btnCadastrarFuncActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarFuncActionPerformed
        String nome = nomeFuncField.getText().trim();
        String cpf = cpfFuncField.getText().trim();
        String endereco = enderecoFuncField.getText().trim();
        String telefone = telefoneFuncField.getText().trim();
        String nivel = cargoField.getText().trim();

        if (validaCampos(nome, cpf, telefone, endereco, nivel)) {
            Funcionarios novoFuncionario = new Funcionarios();
            novoFuncionario.setNome(nome);
            novoFuncionario.setCpf(cpf);
            novoFuncionario.setEndereco(endereco);
            novoFuncionario.setTelefone(telefone);
            novoFuncionario.setCargo(nivel);

            gerenciadorFunc.inserirCliente(novoFuncionario);
           
            limparCampos();
        }
    }//GEN-LAST:event_btnCadastrarFuncActionPerformed

      public boolean validaCampos(String nome, String cpf, String telefone, String endereco, String nivel){
     if(nome.trim().isEmpty() || cpf.trim().isEmpty() || telefone.trim().isEmpty() || endereco.trim().isEmpty() || nivel.trim().isEmpty()){
         JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos para o cadastro", "ERRO", HEIGHT);
         return false;
     }
     return true;
     }
    
    
